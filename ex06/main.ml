(*
 * E2(i = 1 -> n)(k ^ 2) = ((2 * n + 1) * (n + 1) * n) / 6
 *                       = (2 * (2 * n + 1) * (n + 1) * n) / 12
 * E1(i = 1 -> n)(k) ^ 2 = ((n * (n + 1)) / 2) ^ 2
 *                       = (n ^ 2 * (n + 1) ^ 2) / 4
 *                       = (3 * n ^ 2 * (n + 1) ^ 2) / 12
 *
 * E2 - E1 = 1/12 * n * (n + 1) * (3 * n + 2) * (n - 1)
 *)

let f n = (n * (n + 1) * (3 * n + 2) * (n - 1)) / 12

